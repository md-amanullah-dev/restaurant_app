import mongoose from "mongoose";

const orderUserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: Number,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    },
});

export const orderUser = mongoose.models.orderUser || mongoose.model('orderUser', orderUserSchema);
